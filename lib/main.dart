    ColorScheme.fromSeed(seedColor: const Color.fromARGB(1, 207, 46, 46));
var kDarkColorScheme =
    ColorScheme.fromSeed(seedColor: const Color.fromARGB(1, 207, 46, 46));
void main() {
  runApp(const MyApp());
}

